import type { ExtractedRoute, DetectedFramework } from '../types.ts';

const COLLAPSE_THRESHOLD = 5;

export function formatRoutes(
  routes: ExtractedRoute[],
  framework: DetectedFramework,
): string | null {
  if (routes.length === 0) return null;

  const lines: string[] = [];
  const now = new Date().toISOString().split('T')[0];

  lines.push(`# Routes (generated ${now})`);
  lines.push('');
  lines.push(`**Framework:** ${framework.name}`);
  lines.push(`**Total routes:** ${routes.length}`);
  lines.push('');

  // Group by top-level path segment
  const groups = groupRoutesByPrefix(routes);
  const sortedPrefixes = Object.keys(groups).sort();

  for (const prefix of sortedPrefixes) {
    const groupRoutes = groups[prefix];
    lines.push(`## ${prefix}`);
    lines.push('');

    if (groupRoutes.length > COLLAPSE_THRESHOLD) {
      // Collapse: show summary
      const methods = [...new Set(groupRoutes.map((r) => r.method))].join(',');
      const hasAuth = groupRoutes.some((r) => r.auth);
      lines.push(`| Method | Path | Auth |`);
      lines.push(`|--------|------|------|`);
      lines.push(`| ${methods} | \`${prefix}/*\` (${groupRoutes.length} routes) | ${hasAuth ? 'yes' : ''} |`);

      // Show first 3 as examples
      for (const r of groupRoutes.slice(0, 3)) {
        lines.push(`| ${r.method} | \`${r.path}\` | ${r.auth ? 'auth' : ''} |`);
      }
      lines.push(`| | *...${groupRoutes.length - 3} more* | |`);
    } else {
      lines.push(`| Method | Path | Auth | File |`);
      lines.push(`|--------|------|------|------|`);
      for (const r of groupRoutes) {
        lines.push(`| ${r.method} | \`${r.path}\` | ${r.auth ? 'auth' : ''} | \`${r.filePath}\` |`);
      }
    }

    lines.push('');
  }

  lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
  return lines.join('\n');
}

function groupRoutesByPrefix(routes: ExtractedRoute[]): Record<string, ExtractedRoute[]> {
  const groups: Record<string, ExtractedRoute[]> = {};

  for (const route of routes) {
    // Get first 2 path segments: /api/users/[id] → /api/users
    const parts = route.path.split('/').filter(Boolean);
    const prefix = '/' + parts.slice(0, 2).join('/');

    if (!groups[prefix]) groups[prefix] = [];
    groups[prefix].push(route);
  }

  return groups;
}
