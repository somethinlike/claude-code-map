import type { ImportGraph, ParsedFile } from '../types.ts';
import { countExternalDeps } from '../graph.ts';

const MAX_HOT_FILES = 15;
const MAX_EXTERNAL_DEPS = 10;

/**
 * Format the import graph as compact markdown for AI consumption.
 * Returns null if there are no import edges (nothing to report).
 */
export function formatGraph(
  graph: ImportGraph,
  parsedFiles: Record<string, ParsedFile>,
): string | null {
  if (graph.edges.length === 0) return null;

  const lines: string[] = [];
  const timestamp = new Date().toISOString().split('T')[0];

  // Count files that have at least one import or are imported
  const filesWithImports = new Set<string>();
  for (const edge of graph.edges) {
    filesWithImports.add(edge.from);
    filesWithImports.add(edge.to);
  }

  lines.push(`# Dependency Graph (${timestamp})`);
  lines.push('');
  lines.push(`**Internal edges:** ${graph.edges.length} | **Files with imports:** ${filesWithImports.size}`);
  lines.push('');

  // Hot files table
  if (graph.hotFiles.length > 0) {
    lines.push('## Hot Files (most imported)');
    lines.push('');
    lines.push('| File | Imported By | Imports |');
    lines.push('|------|-------------|---------|');

    const topFiles = graph.hotFiles.slice(0, MAX_HOT_FILES);
    for (const hf of topFiles) {
      lines.push(`| \`${hf.filePath}\` | ${hf.importedBy} | ${hf.imports} |`);
    }

    if (graph.hotFiles.length > MAX_HOT_FILES) {
      lines.push(`| ...${graph.hotFiles.length - MAX_HOT_FILES} more files | | |`);
    }

    lines.push('');
  }

  // External dependencies
  const externalDeps = countExternalDeps(parsedFiles);
  if (externalDeps.length > 0) {
    lines.push('## External Dependencies (top packages)');
    lines.push('');
    lines.push('| Package | Used By |');
    lines.push('|---------|---------|');

    const topDeps = externalDeps.slice(0, MAX_EXTERNAL_DEPS);
    for (const dep of topDeps) {
      lines.push(`| \`${dep.name}\` | ${dep.usedBy} file${dep.usedBy === 1 ? '' : 's'} |`);
    }

    if (externalDeps.length > MAX_EXTERNAL_DEPS) {
      lines.push(`| ...${externalDeps.length - MAX_EXTERNAL_DEPS} more packages | |`);
    }

    lines.push('');
  }

  lines.push(`<!-- Generated by claude-code-map -->`);
  return lines.join('\n');
}
