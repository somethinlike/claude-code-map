import type { ExtractedType } from '../types.ts';
import { groupBy } from '../utils.ts';

const MAX_FIELDS = 5;

export function formatTypes(types: ExtractedType[]): string {
  const lines: string[] = [];
  const now = new Date().toISOString().split('T')[0];

  lines.push(`# Types (generated ${now})`);
  lines.push('');

  if (types.length === 0) {
    lines.push('No types found.');
    lines.push('');
    lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
    return lines.join('\n');
  }

  lines.push(`**Total types:** ${types.length}`);
  lines.push('');

  // Group by file
  const byFile = groupBy(types, (t) => t.filePath);
  const sortedFiles = Object.keys(byFile).sort();

  for (const file of sortedFiles) {
    const fileTypes = byFile[file];
    lines.push(`## ${file}`);
    lines.push('');

    for (const t of fileTypes) {
      switch (t.kind) {
        case 'interface':
          formatInterface(t, lines);
          break;
        case 'enum':
          formatEnum(t, lines);
          break;
        case 'type':
          lines.push(`**type** \`${t.name}\``);
          lines.push('');
          break;
      }
    }
  }

  lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
  return lines.join('\n');
}

function formatInterface(t: ExtractedType, lines: string[]): void {
  lines.push(`### ${t.name} (interface)`);
  lines.push('');

  if (t.fields.length === 0) {
    lines.push('*(empty)*');
    lines.push('');
    return;
  }

  lines.push('| Field | Type | Optional |');
  lines.push('|-------|------|----------|');

  const showing = t.fields.slice(0, MAX_FIELDS);
  for (const field of showing) {
    lines.push(`| \`${field.name}\` | \`${field.type}\` | ${field.optional ? '?' : ''} |`);
  }

  const remaining = t.fields.length - showing.length;
  if (remaining > 0) {
    lines.push(`| | *...and ${remaining} more fields* | |`);
  }

  lines.push('');
}

function formatEnum(t: ExtractedType, lines: string[]): void {
  const members = t.fields.map((f) => f.name);
  if (members.length <= 8) {
    lines.push(`**enum** \`${t.name}\`: ${members.join(' | ')}`);
  } else {
    lines.push(`**enum** \`${t.name}\`: ${members.slice(0, 6).join(' | ')} *+${members.length - 6} more*`);
  }
  lines.push('');
}

