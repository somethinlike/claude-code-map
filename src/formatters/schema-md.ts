import type { ExtractedModel } from '../types.ts';

export function formatSchema(models: ExtractedModel[]): string | null {
  if (models.length === 0) return null;

  const lines: string[] = [];
  const now = new Date().toISOString().split('T')[0];

  lines.push(`# Schema (generated ${now})`);
  lines.push('');
  lines.push(`**ORM:** ${[...new Set(models.map((m) => m.orm))].join(', ')}`);
  lines.push(`**Models:** ${models.length}`);
  lines.push('');

  for (const model of models) {
    const nonRelationFields = model.fields.filter((f) => !f.isRelation);
    const relations = model.fields.filter((f) => f.isRelation);

    // Compact format for simple models (3 or fewer non-relation fields)
    if (nonRelationFields.length <= 3 && relations.length <= 2) {
      const fieldStr = nonRelationFields
        .map((f) => {
          const attrs = f.attributes.length > 0 ? `(${f.attributes.join(',')})` : '';
          return `${f.name}${attrs}`;
        })
        .join(' | ');

      const relStr = relations.length > 0
        ? ' → ' + relations.map((r) => r.type).join(', ')
        : '';

      lines.push(`**${model.name}** ${fieldStr}${relStr}`);
      lines.push('');
      continue;
    }

    // Full format
    lines.push(`## ${model.name}`);
    lines.push('');
    lines.push('| Field | Type | Attributes |');
    lines.push('|-------|------|------------|');

    for (const field of nonRelationFields) {
      const attrs = field.attributes.length > 0 ? field.attributes.join(', ') : '';
      const opt = field.required ? '' : '?';
      lines.push(`| \`${field.name}\` | \`${field.type}${opt}\` | ${attrs} |`);
    }

    if (relations.length > 0) {
      const relStr = relations.map((r) => `${r.name}: ${r.type}`).join(', ');
      lines.push(`| | **Relations:** | ${relStr} |`);
    }

    lines.push('');
  }

  lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
  return lines.join('\n');
}
