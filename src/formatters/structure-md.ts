import type { FileNode, DetectedFramework, SupportedLanguage } from '../types.ts';

interface StructureStats {
  totalFiles: number;
  languages: SupportedLanguage[];
}

export function formatStructure(
  tree: FileNode,
  framework: DetectedFramework,
  stats: StructureStats,
): string {
  const lines: string[] = [];
  const now = new Date().toISOString().split('T')[0];

  lines.push(`# Project Structure (generated ${now})`);
  lines.push('');
  lines.push(`**Framework:** ${framework.name}`);
  lines.push(`**Languages:** ${stats.languages.join(', ')}`);
  lines.push(`**Files indexed:** ${stats.totalFiles}`);
  lines.push('');
  lines.push('```');

  renderTree(tree, '', true, lines);

  lines.push('```');
  lines.push('');

  // Entry points
  if (framework.entryPoints.length > 0) {
    lines.push('## Entry Points');
    lines.push('');
    lines.push('| File | Role |');
    lines.push('|------|------|');
    for (const ep of framework.entryPoints) {
      lines.push(`| \`${ep}\` | ${describeEntryPoint(ep, framework)} |`);
    }
    lines.push('');
  }

  lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
  return lines.join('\n');
}

function renderTree(
  node: FileNode,
  prefix: string,
  isRoot: boolean,
  lines: string[],
): void {
  if (isRoot) {
    lines.push(node.name + '/');
    const sorted = sortChildren(node.children);
    const collapsed = collapseChildren(sorted);
    for (let i = 0; i < collapsed.length; i++) {
      const isLast = i === collapsed.length - 1;
      const child = collapsed[i];
      if (typeof child === 'string') {
        lines.push(`${prefix}${isLast ? '└── ' : '├── '}${child}`);
      } else {
        renderTree(child, prefix + (isLast ? '    ' : '│   '), false, lines);
      }
    }
    return;
  }

  const annotation = node.annotation ? ` [${node.annotation}]` : '';
  const suffix = node.isDirectory ? '/' : annotation;
  lines.push(`${prefix.slice(0, -4)}${prefix.endsWith('    ') ? '└── ' : '├── '}${node.name}${suffix}`);

  if (node.isDirectory && node.children.length > 0) {
    const sorted = sortChildren(node.children);
    const collapsed = collapseChildren(sorted);
    const childPrefix = prefix;

    for (let i = 0; i < collapsed.length; i++) {
      const isLast = i === collapsed.length - 1;
      const child = collapsed[i];
      if (typeof child === 'string') {
        lines.push(`${childPrefix}${isLast ? '└── ' : '├── '}${child}`);
      } else {
        renderTree(child, childPrefix + (isLast ? '    ' : '│   '), false, lines);
      }
    }
  }
}

export function sortChildren(children: FileNode[]): FileNode[] {
  return [...children].sort((a, b) => {
    // Directories first
    if (a.isDirectory && !b.isDirectory) return -1;
    if (!a.isDirectory && b.isDirectory) return 1;
    return a.name.localeCompare(b.name);
  });
}

const MAX_VISIBLE_CHILDREN = 8;

export function collapseChildren(children: FileNode[]): (FileNode | string)[] {
  if (children.length <= MAX_VISIBLE_CHILDREN) return children;

  const result: (FileNode | string)[] = [];
  // Show first 5 then collapse
  for (let i = 0; i < 5; i++) {
    result.push(children[i]);
  }
  result.push(`...${children.length - 5} more`);
  return result;
}

function describeEntryPoint(filePath: string, framework: DetectedFramework): string {
  if (filePath.includes('layout')) return 'Root layout';
  if (filePath.includes('_app')) return 'App wrapper';
  if (filePath === 'manage.py') return 'Django management';
  if (filePath.includes('index.astro')) return 'Home page';
  if (filePath.includes('server') || filePath.includes('app')) return 'Server entry';
  return 'Entry point';
}
