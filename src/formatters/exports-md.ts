import type { ExtractedSymbol } from '../types.ts';

const MAX_EXPORTS_PER_FILE = 4;

export function formatExports(symbols: ExtractedSymbol[]): string {
  const lines: string[] = [];
  const now = new Date().toISOString().split('T')[0];

  lines.push(`# Exports (generated ${now})`);
  lines.push('');

  const exported = symbols.filter((s) => s.isExported);

  if (exported.length === 0) {
    lines.push('No exported symbols found.');
    lines.push('');
    lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
    return lines.join('\n');
  }

  lines.push(`**Total exports:** ${exported.length}`);
  lines.push('');

  // Group by file
  const byFile = groupBy(exported, (s) => s.filePath);
  const sortedFiles = Object.keys(byFile).sort();

  for (const file of sortedFiles) {
    const fileSymbols = byFile[file];
    lines.push(`## ${file}`);
    lines.push('');

    // Sort: functions first, then classes, then types, then constants
    const sorted = sortSymbols(fileSymbols);

    const showing = sorted.slice(0, MAX_EXPORTS_PER_FILE);
    const remaining = sorted.length - showing.length;

    lines.push('| Export | Kind | Signature |');
    lines.push('|--------|------|-----------|');

    for (const sym of showing) {
      lines.push(`| \`${sym.name}\` | ${sym.kind} | \`${sym.signature}\` |`);
    }

    if (remaining > 0) {
      lines.push(`| | | *+${remaining} more* |`);
    }

    lines.push('');
  }

  lines.push(`<!-- Generated by claude-code-map at ${new Date().toISOString()} -->`);
  return lines.join('\n');
}

function sortSymbols(symbols: ExtractedSymbol[]): ExtractedSymbol[] {
  const order: Record<string, number> = {
    function: 0,
    method: 1,
    class: 2,
    interface: 3,
    type: 4,
    enum: 5,
    constant: 6,
    variable: 7,
  };

  return [...symbols].sort((a, b) => {
    const orderDiff = (order[a.kind] ?? 99) - (order[b.kind] ?? 99);
    if (orderDiff !== 0) return orderDiff;
    return a.name.localeCompare(b.name);
  });
}

function groupBy<T>(items: T[], key: (item: T) => string): Record<string, T[]> {
  const groups: Record<string, T[]> = {};
  for (const item of items) {
    const k = key(item);
    if (!groups[k]) groups[k] = [];
    groups[k].push(item);
  }
  return groups;
}
