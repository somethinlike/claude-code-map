import type { AuditReport, AuditFinding, AuditSeverity } from '../types.ts';

const MAX_TOP_PRIORITY = 10;

const SEVERITY_ORDER: AuditSeverity[] = ['critical', 'high', 'medium', 'low'];

const SEVERITY_LABELS: Record<AuditSeverity, string> = {
  critical: 'Critical',
  high: 'High',
  medium: 'Medium',
  low: 'Low',
};

/**
 * Format an audit report as compact markdown for AI consumption.
 * Returns null if there are no findings (nothing to report).
 */
export function formatAudit(report: AuditReport): string | null {
  if (report.findings.length === 0) return null;

  const lines: string[] = [];
  const timestamp = new Date().toISOString().split('T')[0];

  lines.push(`# Code Audit (${timestamp})`);
  lines.push('');
  lines.push(
    `**Files analyzed:** ${report.stats.filesAnalyzed} | ` +
      `**Rules run:** ${report.stats.rulesRun} | ` +
      `**Findings:** ${report.findings.length}`,
  );
  lines.push('');

  // Severity breakdown
  const breakdown = SEVERITY_ORDER
    .filter((s) => report.stats.bySeverity[s] > 0)
    .map((s) => `${report.stats.bySeverity[s]} ${s}`)
    .join(', ');
  lines.push(`**Severity:** ${breakdown}`);
  lines.push('');

  lines.push(
    '> Structural red flags that commonly indicate AI-generated code smells.',
  );
  lines.push(
    '> Ranked by severity × import heat — hotter files surface first.',
  );
  lines.push(
    '> This is an opinion-forming tool, not a linter. Treat each finding as "look here first", not a verdict.',
  );
  lines.push('');

  // Top Priority section — the ranked list Claude reads first
  lines.push('## Top Priority');
  lines.push('');
  const top = report.findings.slice(0, MAX_TOP_PRIORITY);
  lines.push('| # | Score | Severity | Rule | File | Hot |');
  lines.push('|---|-------|----------|------|------|-----|');
  top.forEach((f, i) => {
    lines.push(
      `| ${i + 1} | ${f.score} | ${SEVERITY_LABELS[f.severity]} | ${f.rule} | \`${f.filePath}\` | ${f.hotness} |`,
    );
  });
  if (report.findings.length > MAX_TOP_PRIORITY) {
    lines.push(`| | | | | ...${report.findings.length - MAX_TOP_PRIORITY} more | |`);
  }
  lines.push('');

  // Findings grouped by severity
  lines.push('## Findings by Severity');
  lines.push('');

  for (const severity of SEVERITY_ORDER) {
    const bucket = report.findings.filter((f) => f.severity === severity);
    if (bucket.length === 0) continue;

    lines.push(`### ${SEVERITY_LABELS[severity]} (${bucket.length})`);
    lines.push('');

    for (const finding of bucket) {
      lines.push(formatFinding(finding));
      lines.push('');
    }
  }

  lines.push('<!-- Generated by claude-code-map -->');
  return lines.join('\n');
}

function formatFinding(finding: AuditFinding): string {
  const lines: string[] = [];
  const heat = finding.hotness > 0 ? ` [hot: ${finding.hotness}]` : '';
  lines.push(`#### \`${finding.filePath}\` — ${finding.title}${heat}`);
  for (const signal of finding.signals) {
    lines.push(`- ${signal}`);
  }
  lines.push(`- **Action:** ${finding.action}`);
  if (finding.relatedFiles.length > 0) {
    const displayed = finding.relatedFiles.slice(0, 5);
    const extra = finding.relatedFiles.length - displayed.length;
    const related = displayed.map((f) => `\`${f}\``).join(', ') +
      (extra > 0 ? `, +${extra} more` : '');
    lines.push(`- **Related:** ${related}`);
  }
  return lines.join('\n');
}
